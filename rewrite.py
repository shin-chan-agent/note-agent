from google import genai

def rewrite_article(client, article, latest_info, evaluation):

    prompt = f"""
以下の記事を改善してください。

【最新情報】
{latest_info}

上記の最新情報は維持してください。
古い情報へ戻さないでください。

【記事】
{article}

【品質評価結果】
{evaluation}

上記の評価結果を確認し、改善が必要と指摘された項目だけを修正してください。

修正対象には以下が含まれる場合があります。
・品質
・SEO
・最新情報との整合性
・過去記事との重複

評価で問題ない（OK）と判定された項目は変更しないでください。
NGまたは改善点として指摘された内容のみを修正してください。

記事全体を書き直すのではなく、必要な箇所だけを改善してください。

記事のタイトル・構成・見出し・固定記事への案内・ハッシュタグは維持してください。
品質が高い部分は変更しないでください。

現在の記事より品質が下がる書き換えは禁止です。
改善点として指摘された箇所のみ修正してください。

現在の記事の文字数から±10%以内でリライトしてください。
情報を追加しすぎて文字数を大幅に増やすことは禁止です。

内容を増やすことよりも、
・情報の正確性
・分かりやすさ
・SEO
・文章の自然さ
・過去記事との差別化
を優先してください。
"""

    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt,
    )

    return response.text
