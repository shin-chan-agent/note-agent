from google import genai

def rewrite_article(client, article, latest_info, evaluation):

    prompt = f"""
以下の記事は全体として品質の高い記事です。

記事全体を書き直すことは禁止です。
必要最小限の修正だけを行ってください。

【最新情報】
{latest_info}

上記の最新情報は維持してください。
古い情報へ戻さないでください。

【記事】
{article}

【改善指示】
{evaluation}

上記は改善が必要な箇所だけを抜き出した指示です。
記載された内容だけを修正してください。
改善指示に含まれない文章・表現・見出し・順番は変更しないでください。

記事のタイトル・構成・見出し・固定記事への案内・ハッシュタグは維持してください。
品質が高い部分は一切変更しないでください。

現在の記事より品質が下がる書き換えは禁止です。

現在の記事の文字数から±10%以内でリライトしてください。
情報を追加しすぎて文字数を大幅に増やすことは禁止です。

内容を増やすことよりも、
・情報の正確性
・分かりやすさ
・SEO
・過去記事との差別化
・文章の自然さ
を優先してください。

修正対象以外の文章・表現・見出し・順番は、原則として変更しないでください。
修正箇所に伴って前後の文章を自然につなげるための最小限の変更のみ許可します。
"""

    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt,
    )

    return response.text
