from google import genai

def rewrite_article(client, article, latest_info, evaluation):

    prompt = f"""
以下の記事を改善してください。

【最新情報】
{latest_info}

上記の最新情報は維持してください。
古い情報へ戻さないでください。

【記事】
{article}

【指示指示】
{evaluation}

修正指示に書かれている項目だけを修正してください。

修正指示に含まれない文章は変更しないでください。

修正対象には以下が含まれる場合があります。
・品質
・SEO
・最新情報との整合性
・過去記事との重複

評価で問題ない（OK）と判定された項目は変更しないでください。
NGまたは改善点として指摘された内容のみを修正してください。

記事全体を書き直してはいけません。
修正対象以外の文章・表現・見出し・順番は一文字も変更しないでください。

記事のタイトル・構成・見出し・固定記事への案内・ハッシュタグは維持してください。
品質が高い部分は変更しないでください。

現在の記事より品質が下がる書き換えは禁止です。
改善点として指摘された箇所のみ修正してください。

現在の記事の文字数から±10%以内でリライトしてください。
情報を追加しすぎて文字数を大幅に増やすことは禁止です。

内容を増やすことよりも、
・情報の正確性
・分かりやすさ
・SEO
・文章の自然さ
・過去記事との差別化
を優先してください。

修正後の記事は、修正指示に該当する箇所以外は元の記事と完全に一致している状態にしてください。

品質を下げる変更は禁止です。
"""

    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt,
    )

    return response.text
