from google.genai import types

from utils.gemini_client import call_gemini


def quality_check(
    client,
    article,
    past_articles,
    latest_info,
):

    prompt = f"""
以下の記事を総合評価してください。

【記事】
{article}

以下の3項目を評価してください。

①品質
②SEO
③過去記事との重複

---

【評価時の前提】

・この記事は2,500〜5,000文字程度を想定しています。
・この文字数制約の中で最高品質になっているかを評価してください。
・文字数を超えるような改善案は提案しないでください。
・改善点は「必須」と「推奨」を区別してください。

---

【品質評価】

次の5項目を20点満点で採点してください。

・読みやすさ
・独自性
・初心者への分かりやすさ
・具体性
・論理性

合計100点満点で評価してください。

情報の正確性を最優先で評価してください。

---

【SEO評価】

100点満点で評価してください。

以下を確認してください。

・タイトルに主要キーワードが自然に含まれているか
・タイトルがクリックしたくなる内容か
・導入文100文字以内に主要キーワードがあるか
・H2・H3にSEOキーワードが自然に含まれているか
・見出しだけ読んでも内容が分かる構成か
・Google検索とnote検索の両方を意識できているか
・キーワードを不自然に詰め込み過ぎていないか

---


【過去記事（タイトル・テーマ・切り口）】

{past_articles}

---

【重複チェック】

【重複チェック】

上記の過去記事（タイトル・テーマ・切り口）と比較し、以下を総合的に評価してください。

・テーマ
・切り口
・タイトル
・構成
・見出し
・具体例
・まとめ

タイトルだけ違っても、テーマ・切り口・構成が類似している場合は重複と判断してください。

逆に、テーマが同じでも切り口が十分異なり、読者が別の記事として価値を感じる内容であれば重複ではありません。

問題なければ
「DUPLICATE:OK」

問題がある場合は
「DUPLICATE:NG」

と出力し、どの記事とどの部分が重複しているかを改善点へ具体的に記載してください。

---

【最新情報】

{latest_info}

---

【最新情報チェック】

上記の最新情報と記事を比較してください。

・料金
・プラン
・対応モデル
・機能
・仕様
・注意点

に誤りや古い情報がないか確認してください。

問題がなければ
「LATEST: OK」

問題がある場合は
「LATEST: NG」

と出力し、改善点へ修正内容を具体的に記載してください。

---

【出力形式】

必ず以下の形式で出力してください。

SCORE:◯◯
SEO:◯◯
DUPLICATE: OK または NG
LATEST: OK または NG

改善点

【必須】
・○○

【推奨】
・○○

この見出し名・順番を変更しないでください。

品質・SEO・重複・最新情報のすべてに問題がない場合のみ、
「改善点なし」
と出力してください。

DUPLICATE:NG の場合は、どの記事とどこが重複しているのかを改善点へ具体的に記載してください。

【必須】には以下のみを記載してください。

・事実誤認
・最新情報との不一致
・SEO上重大な問題
・記事構成上の重大な問題
・重複の問題
・読者の理解を大きく妨げる問題

【推奨】には以下を記載してください。

・具体例を追加すると良い
・比較表があると良い
・補足するとより理解しやすい
・表現をより自然にできる

記事は2,500〜5,000文字程度を前提としてください。

その文字数を超えることを前提とした改善案は提案しないでください。

出力は上記の形式のみとし、前置き・解説・補足は出力しないでください。

"""

    response = call_gemini(
        client,
        model="gemini-2.5-flash",
        contents=prompt,
    )

    return response.text