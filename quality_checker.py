from google.genai import types

def quality_check(client, article, past_articles):
    prompt = f"""
以下の記事を総合評価してください。

【記事】
{article}

以下の3項目を評価してください。

①品質
②SEO
③過去記事との重複

---

【品質評価】

次の5項目を20点満点で採点してください。

・読みやすさ
・独自性
・初心者への分かりやすさ
・具体性
・論理性

合計100点満点で評価してください。

情報の正確性を最優先で評価してください。

---

【SEO評価】

100点満点で評価してください。

以下を確認してください。

・タイトルに主要キーワードが自然に含まれているか
・タイトルがクリックしたくなる内容か
・導入文100文字以内に主要キーワードがあるか
・H2・H3にSEOキーワードが自然に含まれているか
・見出しだけ読んでも内容が分かる構成か
・Google検索とnote検索の両方を意識できているか
・キーワードを不自然に詰め込み過ぎていないか

---

【過去記事】

{past_articles}

---

【重複チェック】

上記の過去記事と比較し、

・テーマ
・切り口
・構成
・タイトル
・見出し
・具体例
・まとめ

が実質的に重複していないか評価してください。

問題なければ
「DUPLICATE:OK」
重複している場合は
「DUPLICATE:NG」
と出力し、続けて重複している理由を具体的に説明してください。

タイトルや表現が異なっていても、読者が「ほぼ同じ内容の記事」と感じる場合は
「DUPLICATE:NG」
としてください。

---

【出力形式】

必ず以下の形式で出力してください。

SCORE:◯◯
SEO:◯◯
DUPLICATE:OK　または　NG

改善点
・○○
・○○
・○○

この見出し名・順番を変更しないでください。

品質・SEO・重複のすべてに問題がない場合のみ、
「改善点なし」
と出力してください。

DUPLICATE:NG の場合は、どの記事と、どこが重複しているのかを改善点へ具体的に記載してください。

出力は上記の形式のみとし、前置き・解説・補足は出力しないでください。

"""

    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt,

    )

    return response.text